package me.i509.fabric.projectf.mixin.compat;

import java.util.List;
import java.util.Set;
import me.i509.fabric.projectf.ProjectF;
import net.fabricmc.loader.api.FabricLoader;
import org.objectweb.asm.tree.ClassNode;
import org.spongepowered.asm.mixin.extensibility.IMixinConfigPlugin;
import org.spongepowered.asm.mixin.extensibility.IMixinInfo;

public class CompatMixinPlugin implements IMixinConfigPlugin {
	private static final String TRINKETS_MODID = "trinkets";
	private static final String TRINKETS_MIXIN_PACKAGE = "me.i509.fabric.projectf.mixin.compat.trinkets";
	private boolean trinkets;

	@Override
	public void onLoad(String mixinPackage) {
		this.trinkets = FabricLoader.getInstance().isModLoaded(CompatMixinPlugin.TRINKETS_MODID);
	}

	@Override
	public String getRefMapperConfig() {
		return null;
	}

	@Override
	public boolean shouldApplyMixin(String targetClassName, String mixinClassName) {
		if (mixinClassName.startsWith(CompatMixinPlugin.TRINKETS_MIXIN_PACKAGE)) {
			if (this.trinkets) {
				ProjectF.getLogger().info("Loaded Trinkets Compat.");
				return true;
			} else {
				return false;
			}
		}

		return true;
	}

	@Override
	public void acceptTargets(Set<String> myTargets, Set<String> otherTargets) {
	}

	@Override
	public List<String> getMixins() {
		return null;
	}

	@Override
	public void preApply(String targetClassName, ClassNode targetClass, String mixinClassName, IMixinInfo mixinInfo) {
	}

	@Override
	public void postApply(String targetClassName, ClassNode targetClass, String mixinClassName, IMixinInfo mixinInfo) {
	}
}
